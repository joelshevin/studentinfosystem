    private void fillCombo(){
        try{
            String sql = "Select * from program";
            pst= con.prepareStatement(sql);
            ResultSet r1 = pst.executeQuery();
            String name;
            while(r1.next()){
                if(r1.getString("specialization")==null){
                    name = r1.getString("name");
                }
                else{
                    name = r1.getString("name")+" "+r1.getString("specialization");
                }
                degree.addItem(name);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    
    }

