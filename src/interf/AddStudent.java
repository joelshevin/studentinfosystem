    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        String first_name = fname.getText();
        String middle_name = mname.getText();
        String last_name = lname.getText();
        String address = ad.getText();
        String phone = tpnum.getText();
        String date_of_birth = dob.getText();
        String mail = email.getText();
        String p = degree.getSelectedItem().toString();
        
        if(first_name.equals("") || middle_name.equals("") || last_name.equals("") || address.equals("") || phone.equals("") || date_of_birth.equals("") || mail.equals("") || p.equals(""))
        {
            JOptionPane.showMessageDialog(null, "All Fields Must Be Filled!");
        }
        else if (!Validation.isValidBirthDate(date_of_birth))
            {
                JOptionPane.showMessageDialog(null, "Please enter a Valid Date!");
            }
            else if(!Validation.validateNumber(phone)||phone.length()!=10){
                    JOptionPane.showMessageDialog(null, "Please enter a Valid Telephone Number!");
                }
                else
                {
                    student s = new student(con,pst);
                    sid = s.generateIdNumber(con, pst, p);
                    s.addNewStudent(sid, first_name, middle_name, last_name, address, phone, date_of_birth, mail, p,1);
                }
    }//GEN-LAST:event_jButton1ActionPerformed

